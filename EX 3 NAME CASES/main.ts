// Name Cases: Store a person’s name in a variable, and then print that person’s name in lowercase, uppercase, and titlecase.

console.log("Answer 3");

// Store a person’s name in a variable
let firstName:  string = "Muskan";

// print the person’s name in lowercase
console.log("Lowercase:  ",firstName.toLowerCase());

// print the person’s name in uppercase
console.log("Uppercase:  ",firstName.toUpperCase());

// print the person’s name in titlecase
console.log("Titlecase: ", firstName.charAt(0).toUpperCase() + firstName.slice(1).toLowerCase());



